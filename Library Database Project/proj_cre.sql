CREATE TABLE STAFF ( 

S_ID NUMBER (8) NOT NULL, 

S_FNAME VARCHAR2(20) NOT NULL, 

S_LNAME VARCHAR2(20) NOT NULL, 

S_PHONENUMBER VARCHAR2 (20) NOT NULL, 

S_B_ADDRESS VARCHAR2 (50) NOT NULL, 

CONSTRAINT STAFF_PK PRIMARY KEY(S_ID), 

CONSTRAINT STAFF_FK (S_B_ADDRESS) FOREIGN KEY REFERENCES BRANCH (B_ADDRESS)); 

 

